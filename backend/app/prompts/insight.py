def build_insight_prompt(raw_input: str, existing_tags: list[str]) -> str:
    tags_str = "、".join(existing_tags) if existing_tags else "（暂无已有标签）"
    return (
        "你是一个知识整理引擎。用户会输入一段口语化的经验/心得/技术总结，"
        "请你完成以下工作：\n"
        "\n"
        "用户原始输入：\n"
        f"{raw_input}\n"
        "\n"
        "已有标签列表：\n"
        f"{tags_str}\n"
        "\n"
        "请以 JSON 格式返回：\n"
        "- content: 将用户的口语化表述改写为专业、清晰、结构化的内容。"
        "保留原意，提升表达质量。如果内容涉及技术概念，使用准确的术语。"
        "适当分段，必要时使用列表格式。\n"
        "- summary: 一句话摘要（不超过 50 字），概括这条经验的核心要点\n"
        "- tags: 推荐标签数组（1-3 个）。优先从已有标签中选择匹配的，"
        "只有当已有标签都不合适时才创建新标签。"
        '新标签名称应简洁通用（如\u201c机器学习\u201d而非\u201c机器学习中的注意力机制\u201d）。\n'
        "\n"
        "注意：\n"
        "1. 改写时保留用户的核心观点和结论，不要添加用户没有表达的内容\n"
        "2. 如果用户输入过于简短或模糊，在 content 中适当展开，"
        '但标注\u201c（基于原始输入推断）\u201d\n'
        '3. 标签粒度适中，避免过于宽泛（如\u201c技术\u201d）'
        '或过于细碎（如\u201cPython3.11的match语句\u201d）\n'
        "\n"
        "直接返回 JSON，不要包裹在 markdown 代码块中，不要添加任何解释文字。"
    )
